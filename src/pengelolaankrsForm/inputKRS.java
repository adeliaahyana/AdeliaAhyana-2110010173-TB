        public void tampilDataKrs() {
         try {
            tableModel.setRowCount(0);

            String query = "SELECT * FROM krs ";
            try (PreparedStatement preparedStatement = (PreparedStatement) obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[7];
                    line[0] = data.getString("id_matkul");
                    line[1] = data.getString("mata_kuliah");
                    line[2] = data.getString("sks");
                    line[3] = data.getString("kelas");
                    line[4] = data.getString("jurusan");
                    line[5] = data.getString("semester");
                    line[6] = data.getString("dosen");
                    tableModel.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

