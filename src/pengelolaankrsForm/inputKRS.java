    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
    if (idTF.getText().isEmpty() || matkulTF.getText().isEmpty() ||
        sksCB.getSelectedItem() == null || kelasCB.getSelectedItem() == null ||
        jurusanCB.getSelectedItem() == null || semCB.getSelectedItem() == null || dosenTF.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
    } else {
        // Cek apakah data dengan id_matkul yang sama sudah ada
        String sqlCheck = "SELECT * FROM krs WHERE id_matkul=?";
        PreparedStatement checkStmt = (PreparedStatement) obj.konek.prepareStatement(sqlCheck);
        checkStmt.setString(1, idTF.getText());
        ResultSet resultSet = checkStmt.executeQuery();

        if (resultSet.next()) {
            // Data sudah ada, tampilkan pesan kesalahan
            JOptionPane.showMessageDialog(this, "Data dengan ID Mata Kuliah tersebut sudah ada. Gunakan tombol Edit untuk mengubah data.");
        } else {
            // Data belum ada, jalankan perintah INSERT
            String sqlInsert = "INSERT INTO krs (id_matkul, mata_kuliah, sks, kelas, jurusan, semester, dosen) " +
                               "VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement insertStmt = (PreparedStatement) obj.konek.prepareStatement(sqlInsert);

            insertStmt.setString(1, idTF.getText());
            insertStmt.setString(2, matkulTF.getText());
            insertStmt.setString(3, (String) sksCB.getSelectedItem());
            insertStmt.setString(4, (String) kelasCB.getSelectedItem());
            insertStmt.setString(5, (String) jurusanCB.getSelectedItem());
            insertStmt.setString(6, (String) semCB.getSelectedItem());
            insertStmt.setString(7, dosenTF.getText());

            // Eksekusi query INSERT
            int hasilInsert = insertStmt.executeUpdate();

            if (hasilInsert > 0) {
                tampilDataKrs();
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data.");
            }
        }
    }
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
     
    }//GEN-LAST:event_btnSimpanActionPerformed

