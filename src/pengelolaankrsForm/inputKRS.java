    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
            if (idTF.getText().isEmpty() || matkulTF.getText().isEmpty() ||
                sksCB.getSelectedItem() == null || kelasCB.getSelectedItem() == null ||
                jurusanCB.getSelectedItem() == null || semCB.getSelectedItem() == null || dosenTF.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
            } else {
                // Cek apakah data dengan id_matkul yang sama sudah ada
                String sqlCheck = "SELECT * FROM krs WHERE id_matkul=?";
                PreparedStatement checkStmt = (PreparedStatement) obj.konek.prepareStatement(sqlCheck);
                checkStmt.setString(1, idTF.getText());
                ResultSet resultSet = checkStmt.executeQuery();

                if (resultSet.next()) {
                    // Data sudah ada, jalankan perintah UPDATE
                    String sqlUpdate = "UPDATE krs SET mata_kuliah=?, sks=?, kelas=?, jurusan=?, " +
                                       "semester=?, dosen=? WHERE id_matkul=?";
                    PreparedStatement updateStmt = (PreparedStatement) obj.konek.prepareStatement(sqlUpdate);

                    updateStmt.setString(1, matkulTF.getText());
                    updateStmt.setString(2, (String) sksCB.getSelectedItem());
                    updateStmt.setString(3, (String) kelasCB.getSelectedItem());
                    updateStmt.setString(4, (String) jurusanCB.getSelectedItem());
                    updateStmt.setString(5, (String) semCB.getSelectedItem());
                    updateStmt.setString(6, dosenTF.getText());
                    updateStmt.setString(7, idTF.getText());

                    // Eksekusi query UPDATE
                    int hasilUpdate = updateStmt.executeUpdate();

                    if (hasilUpdate > 0) {
                        tampilDataKrs();
                        JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan ID Mata Kuliah yang dimasukkan benar.");
                    }
                } else {
                    // Data belum ada, tampilkan pesan kesalahan
                    JOptionPane.showMessageDialog(this, "Data dengan ID Mata Kuliah tersebut belum ada. Gunakan tombol Tambah untuk menambahkan data baru.");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUbahActionPerformed

    private void tableKrsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKrsMouseClicked
        // TODO add your handling code here:
        int baris = tableKrs.getSelectedRow();
        if (baris != -1) {
            String id_matkul = tableKrs.getValueAt(baris, 0).toString();
            String mata_kuliah = tableKrs.getValueAt(baris, 1).toString();
            String sks = tableKrs.getValueAt(baris, 2).toString();
            String kelas = tableKrs.getValueAt(baris, 3).toString();
            String jurusan = tableKrs.getValueAt(baris, 4).toString();
            String semester = tableKrs.getValueAt(baris, 5).toString();
            String dosen = tableKrs.getValueAt(baris, 6).toString();

            idTF.setText(id_matkul);
            matkulTF.setText(mata_kuliah);

            // Pastikan item yang dipilih pada ComboBox sesuai dengan data yang diambil dari tabel
            sksCB.setSelectedItem(sks);
            kelasCB.setSelectedItem(kelas);
            jurusanCB.setSelectedItem(jurusan);
            semCB.setSelectedItem(semester);
            dosenTF.setText(dosen);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris terlebih dahulu.");
        }

    }//GEN-LAST:event_tableKrsMouseClicked

