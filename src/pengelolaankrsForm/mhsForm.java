    private void tableTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTBMouseClicked
        // TODO add your handling code here:
        int baris = tableTB.getSelectedRow();
        if (baris != -1) {
            String npm = tableTB.getValueAt(baris, 0).toString();
            String nama = tableTB.getValueAt(baris, 1).toString();
            String alamat = tableTB.getValueAt(baris, 2).toString();
            String kelas = tableTB.getValueAt(baris, 3).toString();
            String jurusan = tableTB.getValueAt(baris, 4).toString();
            String jenis_kelamin = tableTB.getValueAt(baris, 5).toString();

            npmTF.setText(npm);
            namaTF.setText(nama);
            alamatTA.setText(alamat);
            kelasCB.setSelectedItem(kelas);
            JurusanCB.setSelectedItem(jurusan);
            JKcb.setSelectedItem(jenis_kelamin);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris terlebih dahulu.");
        }

    }//GEN-LAST:event_tableTBMouseClicked

