    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
            if (npmTF.getText().isEmpty() || namaTF.getText().isEmpty() ||
                alamatTA.getText().isEmpty() || kelasCB.getSelectedItem() == null ||
                JKcb.getSelectedItem() == null || JurusanCB.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
            } else {
                // Cek apakah data dengan NPM yang sama sudah ada
                String sqlCheck = "SELECT * FROM mahasiswa WHERE npm=?";
                PreparedStatement checkStmt = (PreparedStatement) obj.konek.prepareStatement(sqlCheck);
                checkStmt.setString(1, npmTF.getText());
                ResultSet resultSet = checkStmt.executeQuery();

                if (resultSet.next()) {
                    // Data sudah ada, jalankan perintah UPDATE
                    String sqlUpdate = "UPDATE mahasiswa SET nama=?, alamat=?, kelas=?, jurusan=?, " +
                                       "jenis_kelamin=? WHERE npm=?";
                    PreparedStatement updateStmt = (PreparedStatement) obj.konek.prepareStatement(sqlUpdate);

                    updateStmt.setString(1, namaTF.getText());
                    updateStmt.setString(2, alamatTA.getText());
                    updateStmt.setString(3, (String) kelasCB.getSelectedItem());
                    updateStmt.setString(4, (String) JurusanCB.getSelectedItem());
                    updateStmt.setString(5, (String) JKcb.getSelectedItem());
                    updateStmt.setString(6, npmTF.getText());

                    // Eksekusi query UPDATE
                    int hasilUpdate = updateStmt.executeUpdate();

                    if (hasilUpdate > 0) {
                        tampilDataMhs();
                        JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan NPM yang dimasukkan benar.");
                    }
                } else {
                    // Data belum ada, tampilkan pesan kesalahan
                    JOptionPane.showMessageDialog(this, "Data dengan NPM tersebut belum ada. Gunakan tombol Tambah untuk menambahkan data baru.");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUbahActionPerformed

